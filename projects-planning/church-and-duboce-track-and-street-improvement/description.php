         <p class="lead">The goal of the John F. Kennedy Drive (JFK) cycle track is to make JFK Drive accessible and safe for all users, including pedestrians and cyclists of all abilities. In public meetings and through our online surveys, San Franciscans expressed their interest in slowing down traffic, increasing safety, and maintaining the family-friendly nature of Golden Gate Park.</p>
         <p>In an effort to maintain and improve your Muni light rail service, the San Francisco Municipal Transportation Agency (SFMTA), which operates Muni, will begin replacing and improving a portion of the rail system for the N Judah and J Church lines starting in spring 2011. Construction is expected to continue for approximately 18 to 24 months.</p>
