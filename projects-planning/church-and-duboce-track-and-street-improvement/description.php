<p>The main scope of the project is to replace Muni track along Duboce Avenue from Noe Street to Church Street, including partial storage tracks on Duboce Avenue outside of the Duboce portal, and along Church Street between Duboce Avenue and Market Street including the rail crossings on Market Street.  The project will also replace and upgrade the Overhead Contact System, which powers the trains, and all of its essential components and replace the existing track switch control and signal system.  This is another major reinvestment of your tax dollars to improve your Muni system.</p>
<p>In addition, underground utility work, including water and sewer improvements, will take place.</p>

<p>Before track work begins, a temporary crossover and a temporary platform for customers with disabilities will be constructed on Church Street, south of Market Street, on a weekend in fall 2011. Some parking spaces in the immediate area will be unavailable during the construction period. </p>


<h3>City And Community Support</h3>	
<div class="fpo" style="height:300px;">Photo/Rendering</div>    
<p>A collaborative effort between various City agencies and local community groups, the Church & Duboce Track and Street Improvement Project is part of the SFMTAâ€™s ongoing Capital Improvement Program, established to ensure a safer, more reliable transportation network for you by replacing worn infrastructure components nearing the end of their useful life.

With the incorporation of community-based input and inter-agency support from the San Francisco Public Utilities Commission, Department of Public Works, City Planning Department, San Francisco Arts Commission and the San Francisco County Transportation Authority, this coordinated joint venture will serve as a model for future capital projects.  By integrating infrastructure and streetscape enhancements along with Better Streets policies, the Church & Duboce Track & Street Improvement Project will create a safer, more attractive, multimodal neighborhood environment.

The Duboce Triangle Neighborhood Association, the Wigg Party and the Castro/Upper Market Community Benefits District provided valuable input on this project.  Working cooperatively with these community groups and sister City agencies, the SFMTA was able to coordinate additional resources that will improve the quality of life in your neighborhood.   </p>


<h3>Project Benefits</h3>	
<p>The project will revitalize your Muni system by upgrading Muni tracks and essential systems and provide numerous streetscape and infrastructure improvements to your neighborhood. Streetscape improvements resulting from this project include:</p>
<ul>
    <li>Sidewalk extensions and special street paving treatments</li>
    <li>Widened and beautified boarding islands with special paving treatments</li>
    <li>Installation of new transit shelters with NextMuni electronic signs on boarding islands</li>
    <li>Painted poles and decorative streetlight fixtures</li>
    <li>A new, dedicated bike lane on Duboce between Church and Fillmore streets</li>
    <li>A new crosswalk along westbound Market Street at Church and 14th streets</li>
    <li>New seating, plantings and public art</li>
    <li>Curb ramp upgrades</li>
    <li>Water and sewer improvements</li>
    <li>Streetlight replacement</li>
</ul>

<h3>Construction Impact</h3>	

<h4>What to Expect</h4>

<p>While the SFMTA Capital Improvement Program is designed to keep all elements of the transportation network in a state of good repair and safe operation, we are aware that there will be inconveniences to you and your neighbors during construction such as:</p>

    <ul>Construction dust and noise</li>
    <li>Weekend work</li>
    <li>Presence of construction equipment and crews</li>
    <li>Periodic traffic lane closures</li>
    <li>Traffic detours</li>
    <li>Temporary removal of some street parking</li>
    <li>Slower traffic</li>
    <li>N Line, J Line, F Line and 22 Fillmore Route bus shuttle substitutions</li>
    <li>Rail to bus shuttle transfers necessary for some destinations on J and N lines</li>
    <li>Increased traffic on side streets</li>
    <li>Longer transit travel times and some stop changes</li>
    <li>Temporary closure of bike lanes</li>
</ul>

<p>To minimize these inconveniences, the SFMTA is implementing a series of measures including:</p>
<ul>
    <li>Working weekend days as well as weekdays to minimize Muni service disruption</li>
    <li>Maintaining peak-hour Muni Metro service levels</li>
    <li>Providing substitute bus service in construction areas</li>
    <li>Using multiple crews to finish construction as quickly as possible</li>
    <li>Providing Traffic Control Officers to maintain traffic flow</li>
    <li>Adhering to City codes to minimize dust and noise</li>
    <li>Providing SFMTA Customer Ambassadors in orange caps and vests to advise customers about substitute bus service and trip planning</li>
    <li>Posting signs directing customers to transfer points between rail and bus service; steering customers from restricted parking areas and directing them to nearby available parking; and advising customers to businesses that are open and accessible during the construction window</li>
    <li>Ensuring that local access is available at all times</li>
</ul>
<h4>How will construction affect local traffic?</h4>

<p>During the construction period, there will be traffic detours around the construction work.  In addition, some street parking will be restricted during construction hours.  Signage will be posted to alert motorists.  Local access will be granted and local businesses will remain open.
Will my Muni service be affected?</p>

<p>Muni service along Duboce Avenue and Church Street will continue for the duration of the reconstruction period; however, service on the J Line, N Line, F Line and 22 Fillmore bus route may be supplemented or substituted with shuttle bus service for limited durations.</p>

<p>In addition, some major shutdowns to the J and N lines and the 22 Fillmore will occur. These include:</p>
<ul>
    <li>Weekend shutdowns for the J and F lines and 22 Fillmore Route starting in fall 2011</li>
    <li>Weekend shutdowns for the N Judah Line starting in spring 2012</li>
    <li>A longer multiple-day shutdown for the N Judah and J Church lines and 22 Fillmore bus route in early summer 2012</li>
</ul>


