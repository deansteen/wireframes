                  <p>Amending Sections 1102, 1103, 1105, 1106, 1108, 1113, 1114 and 1117-1123 of Article 1100 of Division II of the San Francisco Transportation Code to:</p>
                  <ol>
                  <li>add definitions of necessary terms and amend the definitions of A-Card Seniority and Single Operator Part-time Permit</li>
                  <li>add requirements for renewal of Color Scheme permits</li>
                  <li>render a permit application inactive if it is not completed within 60 days</li>
                  <li>require all Gas and Gates Medallion vehicles to change shifts on Color Scheme property</li>
                  <li>authorize the Director of Transportation to impose a moratorium on the issuance of Color Scheme Permits or Dispatch Service Permits</li>
                  <li>prohibit retaliation for exercise of rights provided by Article 1100</li>
                  <li>delete requirement that taxis taken out of service be returned to service in 30 days or be permanently replaced</li>
                  <li>to make each day of the unauthorized use of a spare vehicle by a Color Scheme a separate offense</li>
                  <li>require Color Schemes to report vehicle-related insurance claims received or filed to the SFMTA and to ensure that Gas and Gates Medallions are not violating laws limiting the length of a commercial driver's shift</li>
                  <li>require Color Schemes to provide certain notices to the Paratransit Broker regarding In-Taxi Equipment</li>
                  <li>prohibit certain practices by Drivers in connection with accepting payment by Paratransit Debit Card</li>
                  <li>prohibit Drivers from tampering with required taxi equipment</li>
                  <li>require that taxi security cameras be manufactured after 2006</li>
                  <li>require Color Schemes to provide security camera data to the SFMTA and the SFPD</li>
                  <li>make minor changes to the procedures for hearings on decisions to grant or deny permits</li>
                  <li>renumber and amend Sections 1118 – 1123</li>
                  <li>clarify procedures for hearings on Citations issued to Permit Holders and members of the public</li>
                  <li>clarify SFMTA's procedures for providing public notice to the taxi industry</li>
                  <li>make Color Scheme Permit Holders responsible for ensuring that all Gate Fees charged for use of vehicles affiliated with the Color Scheme are within the Gate Fee cap. (Explanatory documents include a staff report, resolution and amendment.)</li>
